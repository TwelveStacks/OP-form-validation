form.addEventListener('submit', e => {
    e.preventDefault();

    validateInputs();
})

if (!validateEmail(email.value)) {
    emailError.textContent = 'Please enter a valid email address.';
}

if (!countryValidation(country.value)) {
    countryError.textContent = 'Please enter your country.';
}

// Zip Code validation
if (!zipCode.checkValidity()) {
    zipCodeError.textContent = 'Please enter a valid zip code.';
}

// Password validation
if (password.value === '') {
    passwordError.textContent = 'Please enter a password.';
}

// Password Confirmation validation
if (password.value !== passwordConfirmation.value) {
    passwordConfirmationError.textContent = 'Passwords do not match.';
}

email.addEventListener('input', function () {
    if (validateEmail(email.value)) {
        emailError.textContent = '';
    } else if (!validateEmail(email.value)) {
        emailError.textContent = 'Please enter a valid email address.';
    }
})

country.addEventListener('input', function () {
    if (countryValidation(country.value)) {
        countryError.textContent = '';
    } else {
        countryError.textContent = 'Please enter a valid country.'
    }
})

zipCode.addEventListener('input', function () {
    if (zipCode.checkValidity()) {
        zipCodeError.textContent = '';
    } else if (!zipCode.checkValidity()) {
        zipCodeError.textContent = 'Please enter a valid zip code.';
    }
})

password.addEventListener('input', function () {
    if (password.value === '') {
        passwordError.textContent = 'Please enter a password';
    } else {
        passwordError.textContent = '';
    }
})

passwordConfirmation.addEventListener('input', function () {
    if (passwordConfirmation.value !== password.value) {
        passwordConfirmationError.textContent = 'Passwords do not match';
        passwordConfirmation.setCustomValidity('Passwords do not match')
    } else {
        passwordConfirmationError.textContent = '';
        passwordConfirmation.setCustomValidity('');
    }
})

function countryValidation(country) {
    const lowercaseCountry = country.toLowerCase();
    return countryList.some(valid => valid.toLowerCase() === lowercaseCountry);
}

function validateEmail(email) {
    let mailFormat = /^(([^<>()[\]\\.,;:\s@"]+(\.[^<>()[\]\\.,;:\s@"]+)*)|(".+"))@((\[[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\])|(([a-zA-Z\-0-9]+\.)+[a-zA-Z]{2,}))$/;
    if (email.match(mailFormat)) {
        return true;
    }
    else {
        return false;
    }
}
